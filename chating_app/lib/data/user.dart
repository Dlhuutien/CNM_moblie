class ObjectUser {
  final String userID;
  final String soDienThoai;
  final String matKhau;
  final String hoTen;
  final String gender;
  final String birthday;
  final String email;
  final String work;

  ObjectUser({
    required this.userID,
    required this.soDienThoai,
    required this.matKhau,
    required this.hoTen,
    required this.gender,
    required this.birthday,
    required this.email,
    required this.work,
  });
}
